def OpenUSBtxtFileAndParse(FileToRead, ListOfKeysToOutput_argument = list(), PrintFlag = 0):

    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################
    ListOfKeysToOutput_AllVariables = ['DeviceName',
                                       'Description',
                                       'DeviceType',
                                       'Connected',
                                       'SafeToUnplug',
                                       'Disabled',
                                       'USBHub',
                                       'DriveLetter',
                                       'SerialNumber',
                                       'CreatedDate',
                                       'LastPlug/UnplugDate',
                                       'VendorID',
                                       'ProductID',
                                       'FirmwareRevision',
                                       'USBClass',
                                       'USBSubClass',
                                       'USBProtocol',
                                       'Hub/Port',
                                       'ComputerName',
                                       'VendorName',
                                       'ProductName',
                                       'ParentIdPrefix',
                                       'ServiceName',
                                       'ServiceDescription',
                                       'DriverFilename',
                                       'DeviceClass',
                                       'DeviceMfg',
                                       'FriendlyName',
                                       'Power',
                                       'USBVersion',
                                       'DriverDescription',
                                       'DriverVersion',
                                       'DriverInfSection',
                                       'DriverInfPath',
                                       'InstanceID',
                                       'Capabilities']

    ListOfKeysToOutput_selected = []

    #######################################################################################################################
    #######################################################################################################################
    if len(ListOfKeysToOutput_argument) == 0:
        ListOfKeysToOutput_selected = ListOfKeysToOutput_AllVariables
    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    else:

        #######################################################################################################################
        for item in ListOfKeysToOutput_argument:

            #############################################
            if item in ListOfKeysToOutput_AllVariables:
                ListOfKeysToOutput_selected.append(item)
            #############################################

        #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################

    ####################################################################################################################### Read file and put all USB information into a list of strings
    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    try:
        FileToRead_Object = open(FileToRead, 'r')

    except:
        print("Could not locate/open the file " + FileToRead)
        raw_input("Press any key (and enter) to exit.")
        sys.exit()
    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################

    line_list = []
    StringThatContainsAllLines = ""
    File_device_delimiters_ListOfOccurenceIndexes = []
    USBdeviceListOf_StringThatContainsAllLines = [""]
    for line_counter, line in enumerate(FileToRead_Object):
        #######################################################################################################################

        ####################################################
        line = line.replace(",", "|") #Replace commas with another delimeter since we add commas in ourselves in a further step to separate lines from each other
        line = line.replace("\n", ",") #Replace EOL with commas
        line = line.replace(" ","") #Eliminate all spaces
        line = line.replace(":,", ": ,") #Find lines that don't have a space after colon and put one there to make the line parsable
        ####################################################

        ####################################################
        if line.find("==================================================") != -1:

            File_device_delimiters_ListOfOccurenceIndexes.append(line_counter)
            if IsNumberEven(len(File_device_delimiters_ListOfOccurenceIndexes)) == 1: #If we've seen an even number of USB device delimiters '====', then add another list item

                #print str(line_counter) + "," + str(len(File_device_delimiters_ListOfOccurenceIndexes))
                #print StringThatContainsAllLines
                USBdeviceListOf_StringThatContainsAllLines[len(USBdeviceListOf_StringThatContainsAllLines)-1] = StringThatContainsAllLines
                USBdeviceListOf_StringThatContainsAllLines.append("")
                StringThatContainsAllLines = ""

            else:
                pass
        ####################################################

        ####################################################
        if line.find(":") != -1: #Only lines that contain useful data

            if line.find("Date") != -1: #Line contains the word "Date"
                line = line.replace(":", "") #Remove colons from timestamp
                line = line.replace("Date", "Date:") #Add the colon back after the variable 'Date' to make it parsable

            StringThatContainsAllLines = StringThatContainsAllLines + line
        ####################################################

        ####################################################
        line_list.append(line)
        ####################################################

        #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    try:
        FileToRead_Object.close()

    except:
        pass
    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################
    USBdeviceListOf_StringThatContainsAllLines = USBdeviceListOf_StringThatContainsAllLines[0:-1] #Remove the last element because it's blank

    #print len(USBdeviceListOf_StringThatContainsAllLines)
    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################

    ####################################################################################################################### Turn lists of strings into lists of dicts that contain all USB information
    #######################################################################################################################
    #######################################################################################################################
    USBdeviceListOf_Dicts_AllInfo = []

    for item in USBdeviceListOf_StringThatContainsAllLines:
        USBdeviceListOf_Dicts_AllInfo.append(ParseColonCommaSeparatedVariableString(item))

    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################

    #print len(USBdeviceListOf_Dicts_AllInfo)

    ####################################################################################################################### Copy only desired variables from the all-info dict to a new dict
    #######################################################################################################################
    #######################################################################################################################
    USBdeviceListOf_Dicts_SelectedInfo = []
    for ListElemenet_DictOfAllInfo in USBdeviceListOf_Dicts_AllInfo:
        #######################################################################################################################
        #######################################################################################################################

        DictOfSelectedInfo = OrderedDict()
        for key in ListElemenet_DictOfAllInfo:

            #######################################################################################################################
            if key in ListOfKeysToOutput_selected:
                DictOfSelectedInfo[key] = ListElemenet_DictOfAllInfo[key]
            #######################################################################################################################

        USBdeviceListOf_Dicts_SelectedInfo.append(DictOfSelectedInfo)

        if PrintFlag == 1:
            print("\n##########\n" + ConvertDictToProperlyFormattedStringForPrinting(DictOfSelectedInfo) + "##########\n")

        #######################################################################################################################
        #######################################################################################################################

    #######################################################################################################################
    #######################################################################################################################
    #######################################################################################################################

    #print len(USBdeviceListOf_Dicts_SelectedInfo)

    return USBdeviceListOf_Dicts_SelectedInfo
